#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t	i;
	char	*cpy;

	i = 0;
	cpy = (char*)s2;
	while (*s1)
	{
		s1++;
		i++;
	}
	while (*s2)
	{
		*s1 = *s2;
		s1++;
		s2++;
		i++;
	}
	*s1 = 0;
	return (s1 - i);
}
