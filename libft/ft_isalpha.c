int	ft_isalpha(int c)
{
	unsigned char	ch;

	if (c >= 0 && c < 256)
		ch = (unsigned char)c;
	else
		return (0);
	if ((ch >= 'A' && ch <= 'Z') || (ch >= 'a' && ch <= 'z'))
		return (1);
	return (0);
}
