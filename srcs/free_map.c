#include "libft.h"

void	free_map(char ***map)
{
	char	**mem;

	if (*map && **map)
	{
		mem = *map;
		while (**map)
		{
			if (*map && **map)
			{
				free(**map);
				**map = NULL;
			}
			(*map)++;
		}
		*map = mem;
		free(*map);
		*map = NULL;
	}
}
